package orientaçãoobjeto.lista1poo.lista1exercicio4poo;

public class Cidade {
     private String nome;
     private String qtdPopulação;
     private String idh;
     private String pontosTuristicos;

    public Cidade() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getQtdPopulação() {
        return qtdPopulação;
    }

    public void setQtdPopulação(String qtdPopulação) {
        this.qtdPopulação = qtdPopulação;
    }

    public String getIdh() {
        return idh;
    }

    public void setIdh(String idh) {
        this.idh = idh;
    }

    public String getPontosTuristicos() {
        return pontosTuristicos;
    }

    public void setPontosTuristicos(String pontosTuristicos) {
        this.pontosTuristicos = pontosTuristicos;
    }
}
